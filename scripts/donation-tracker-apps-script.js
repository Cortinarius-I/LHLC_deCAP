/**
 * Receives donation confirmation from the website form.
 * Adds a row to the Donations sheet and sends an acknowledgment email.
 */
function doPost(e) {
  try {
    var p = e.parameter;
    var ss    = SpreadsheetApp.getActiveSpreadsheet();
    var sheet = ss.getSheetByName("Donations");
    if (!sheet) {
      setupSheet();
      sheet = ss.getSheetByName("Donations");
    }

    var now = new Date();
    var row = [
      now,                           // A: Timestamp
      p.donor_name    || "",         // B: Donor Name
      p.email         || "",         // C: Email
      p.phone         || "",         // D: Phone
      p.pan           || "",         // E: PAN
      p.address       || "",         // F: Address
      p.donor_type    || "",         // G: Indian / Foreign
      p.amount        || "",         // H: Amount
      p.transfer_date || "",         // I: Date of Transfer
      p.payment_mode  || "",         // J: Payment Mode
      p.transaction_ref || "",       // K: Transaction Ref / UTR
      p.purpose       || "",         // L: Purpose
      p.frequency     || "One-time", // M: Frequency Intent
      p.wants_receipt || "No",       // N: Wants 80G Receipt
      "Pending",                     // O: Status
      "", "", "", "", "",            // P–T: Staff columns
    ];
    sheet.appendRow(row);

    // Send acknowledgment email
    if (p.email) {
      sendAcknowledgmentEmail(p);
    }

    return ContentService
      .createTextOutput(JSON.stringify({ success: true }))
      .setMimeType(ContentService.MimeType.JSON);

  } catch (err) {
    Logger.log(err);
    return ContentService
      .createTextOutput(JSON.stringify({ success: false, error: err.message }))
      .setMimeType(ContentService.MimeType.JSON);
  }
}

// CORS preflight
function doGet() {
  return ContentService
    .createTextOutput(JSON.stringify({ status: "ok" }))
    .setMimeType(ContentService.MimeType.JSON);
}


