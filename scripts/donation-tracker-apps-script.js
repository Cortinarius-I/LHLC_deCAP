/**
 * Fetches /assets/donation-config.json from the live site.
 * All values (org name, PAN, 80G reg, etc.) come from Decap CMS → donation.yml.
 */
function fetchConfig() {
  var response = UrlFetchApp.fetch(CONFIG.configUrl, { muteHttpExceptions: true });
  if (response.getResponseCode() !== 200) {
    throw new Error("Could not fetch config from: " + CONFIG.configUrl + " (status " + response.getResponseCode() + ")");
  }
  return JSON.parse(response.getContentText());
}


