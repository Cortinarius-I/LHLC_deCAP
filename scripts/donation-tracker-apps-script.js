    + '<p style="color:#888;margin:0;font-size:13px;">Little Hearts Learning Centre</p>'
    + '</div>'
    + '<div style="padding:22px 0;">'
    + '<p>Dear <strong>' + (p.donor_name || 'Friend') + '</strong>,</p>'
    + '<p>Thank you for your generous donation of <strong style="color:#4A0E7A;">&#8377;' + formatIndianNumber(p.amount) + '</strong>'
    + ' towards <em>' + purposeText + '</em>.</p>'
    + '<p>We have received your transfer confirmation and will verify the transaction shortly.</p>'
    + receiptLine
    + '<div style="background:#f3eaf8;border-radius:8px;padding:14px 16px;margin:16px 0;font-size:13px;line-height:1.6;">'
    + 'Your contribution directly supports the education, therapy, and care of children with special needs at Little Hearts Learning Centre. We are deeply grateful.'
    + '</div>'
    + '<p style="margin-top:18px;">With gratitude,<br><strong>' + org.authorized_signatory + '</strong><br>'
