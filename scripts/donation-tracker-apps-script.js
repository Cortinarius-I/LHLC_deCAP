function getFinancialYear(date) {
  var d = new Date(date);
  var year = d.getFullYear(), month = d.getMonth();
  if (month < 3) return (year - 1) + "-" + String(year).slice(2);
  return year + "-" + String(year + 1).slice(2);
}

function formatIndianNumber(num) {
  num = Number(num) || 0;
  var s = num.toFixed(0), len = s.length;
  if (len <= 3) return s;
  var result = s.slice(len - 3), rem = s.slice(0, len - 3).split('');
  while (rem.length > 0) {
    var chunk = rem.splice(Math.max(rem.length - 2, 0)).join('');
    result = chunk + ',' + result;
  }
  return result;
}

function numberToWords(num) {
  num = Math.floor(Number(num) || 0);
  if (num === 0) return "Zero";
  var ones = ["","One","Two","Three","Four","Five","Six","Seven","Eight","Nine",
    "Ten","Eleven","Twelve","Thirteen","Fourteen","Fifteen","Sixteen","Seventeen","Eighteen","Nineteen"];
  var tens = ["","","Twenty","Thirty","Forty","Fifty","Sixty","Seventy","Eighty","Ninety"];
  function c(n) {
    if (n < 20)       return ones[n];
    if (n < 100)      return tens[~~(n/10)] + (n%10 ? " " + ones[n%10] : "");
    if (n < 1000)     return ones[~~(n/100)] + " Hundred" + (n%100 ? " and " + c(n%100) : "");
    if (n < 100000)   return c(~~(n/1000)) + " Thousand" + (n%1000 ? " " + c(n%1000) : "");
    if (n < 10000000) return c(~~(n/100000)) + " Lakh" + (n%100000 ? " " + c(n%100000) : "");
    return c(~~(n/10000000)) + " Crore" + (n%10000000 ? " " + c(n%10000000) : "");
  }
  return c(num) + " Only";
}
