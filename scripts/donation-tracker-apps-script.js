/**
 * Generates an 80G receipt PDF and emails it to the donor.
 * Select the donation row first, then use ♥ Donation Tools → Generate Receipt.
 *
 * REQUIRES: Drive API v2 enabled under Services.
 */
function generateReceiptForSelected() {
  var sheet = SpreadsheetApp.getActiveSheet();
  var row   = sheet.getActiveRange().getRow();

  if (row < 2) {
    SpreadsheetApp.getUi().alert("Please select a donation row (not the header).");
    return;
  }

  var data        = sheet.getRange(row, 1, 1, 20).getValues()[0];
  var status      = data[14]; // Column O
  var donorType   = String(data[6]).toLowerCase();
  var wantsReceipt = data[13]; // Column N

  if (status !== "Verified") {
    SpreadsheetApp.getUi().alert(
      "This donation must be marked 'Verified' before generating a receipt.\nCurrent status: " + status
    );
    return;
  }

  if (donorType === "foreign") {
    SpreadsheetApp.getUi().alert(
      "80G receipts are for Indian donors only.\nFor foreign donors, issue an FCRA receipt manually."
    );
    return;
  }

  if (wantsReceipt !== "Yes") {
    var proceed = SpreadsheetApp.getUi().alert(
      "This donor did not request an 80G receipt. Generate and send anyway?",
      SpreadsheetApp.getUi().ButtonSet.YES_NO
    );
    if (proceed !== SpreadsheetApp.getUi().Button.YES) return;
  }

  // Fetch config from live site (CMS-driven)
  var cfg = fetchConfig();
  var org = cfg.org;
  var t80g = cfg.tax_80g;

  // Receipt number
  var settingsSheet = SpreadsheetApp.getActiveSpreadsheet().getSheetByName("Settings");
  var nextNum  = settingsSheet ? settingsSheet.getRange("B2").getValue() : 1;
  var receiptNo = org.receipt_prefix + String(nextNum).padStart(4, "0");

  // Build replacement map
  var replacements = {
    "{{RECEIPT_NO}}":         receiptNo,
    "{{RECEIPT_DATE}}":       Utilities.formatDate(new Date(), "Asia/Kolkata", "dd/MM/yyyy"),
    "{{FINANCIAL_YEAR}}":     getFinancialYear(data[8] || new Date()),
    "{{ORG_NAME}}":           org.name,
    "{{ORG_ADDRESS}}":        org.address,
    "{{ORG_PHONE}}":          org.phone,
    "{{ORG_EMAIL}}":          org.email,
    "{{TRUST_PAN}}":          org.pan,
    "{{AUTHORIZED_SIGNATORY}}": org.authorized_signatory,
    "{{DONOR_NAME}}":         data[1]  || "",
    "{{DONOR_ADDRESS}}":      data[5]  || "",
    "{{DONOR_PAN}}":          data[4]  || "",
    "{{DONOR_EMAIL}}":        data[2]  || "",
    "{{DONOR_PHONE}}":        data[3]  || "",
    "{{AMOUNT_FIGURES}}":     formatIndianNumber(data[7]),
    "{{AMOUNT_WORDS}}":       numberToWords(data[7]),
    "{{DONATION_DATE}}":      data[8] ? Utilities.formatDate(new Date(data[8]), "Asia/Kolkata", "dd/MM/yyyy") : "",
    "{{PAYMENT_MODE}}":       data[9]  || "",
    "{{TRANSACTION_REF}}":    data[10] || "",
    "{{DONATION_PURPOSE}}":   data[11] || "General",
    "{{DONATION_FREQUENCY}}": data[12] || "One-time",
    "{{80G_REG_NO}}":         t80g.registration_number,
    "{{80G_VALID_FROM}}":     t80g.valid_from,
    "{{80G_VALID_TO}}":       t80g.valid_to,
  };

  try {
    // Fetch HTML receipt template from GitHub Pages
    var htmlTemplate = UrlFetchApp.fetch(CONFIG.receiptTemplateUrl).getContentText();

    // Replace all placeholders
    for (var key in replacements) {
      htmlTemplate = htmlTemplate.split(key).join(replacements[key]);
    }

    // Convert HTML → Google Doc → PDF using Drive API v2
    // (Requires Drive API v2 enabled under Services)
    var fileName = "80G_Receipt_" + receiptNo + "_" + (data[1] || "Donor");
    var htmlBlob = Utilities.newBlob(htmlTemplate, 'text/html', fileName + '.html');

    var folder   = DriveApp.getFolderById(CONFIG.receiptFolderId);
    var docFile  = Drive.Files.insert(
      { title: fileName, mimeType: 'application/vnd.google-apps.document', parents: [{ id: CONFIG.receiptFolderId }] },
      htmlBlob,
      { convert: true }
    );

    // Export as PDF
    var pdfBlob = DriveApp.getFileById(docFile.id)
      .getAs('application/pdf')
      .setName(fileName + '.pdf');

    // Save PDF to folder
    folder.createFile(pdfBlob);

    // Email to donor
    var donorEmail = data[2];
    if (donorEmail) {
      GmailApp.sendEmail(donorEmail, org.receipt_email_subject, '', {
        htmlBody:    buildReceiptEmailBody(data[1], data[7], receiptNo, org),
        attachments: [pdfBlob],
        name:        org.name,
        replyTo:     org.email,
      });
    }

    // Update sheet row
    sheet.getRange(row, 15).setValue("Receipt Sent"); // Status
    sheet.getRange(row, 18).setValue(receiptNo);       // Receipt No
    sheet.getRange(row, 19).setValue(new Date());      // Receipt Sent Date

    // Increment receipt counter
    if (settingsSheet) settingsSheet.getRange("B2").setValue(nextNum + 1);

    // Delete the Google Doc copy (keep only the PDF)
    DriveApp.getFileById(docFile.id).setTrashed(true);

    SpreadsheetApp.getActiveSpreadsheet().toast(
      "Receipt " + receiptNo + " sent to " + (donorEmail || "donor"),
      "📧 Receipt Sent"
    );

  } catch (err) {
    SpreadsheetApp.getUi().alert("Error generating receipt:\n" + err.message);
    Logger.log(err);
  }
}

