/**
 * Run once to create the Donations and Settings sheets.
 */
function setupSheet() {
  var ss = SpreadsheetApp.getActiveSpreadsheet();

  // --- Donations sheet ---
  var sheet = ss.getSheetByName("Donations") || ss.insertSheet("Donations");
  var headers = [
    "Timestamp",           // A
    "Donor Name",          // B
    "Email",               // C
    "Phone",               // D
    "PAN",                 // E
    "Address",             // F
    "Donor Type",          // G  — Indian / Foreign
    "Amount",              // H
    "Date of Transfer",    // I
    "Payment Mode",        // J
    "Transaction Ref/UTR", // K
    "Purpose",             // L
    "Frequency Intent",    // M
    "Wants 80G Receipt",   // N
    "Status",              // O  — Pending / Verified / Receipt Sent / Rejected
    "Verified By",         // P
    "Verified Date",       // Q
    "Receipt No",          // R
    "Receipt Sent Date",   // S
    "Notes",               // T
  ];

  sheet.getRange(1, 1, 1, headers.length).setValues([headers]);
  sheet.getRange(1, 1, 1, headers.length)
    .setBackground("#4A0E7A").setFontColor("#FFFFFF").setFontWeight("bold").setFontSize(10);
  sheet.setFrozenRows(1);
  [1,2,3].forEach(function(c) { sheet.setColumnWidth(c, c === 3 ? 210 : 170); });
  sheet.setColumnWidth(8, 100);
  sheet.setColumnWidth(12, 180);
  sheet.setColumnWidth(15, 120);

  var statusRule = SpreadsheetApp.newDataValidation()
    .requireValueInList(["Pending", "Verified", "Receipt Sent", "Rejected"]).build();
  sheet.getRange("O2:O1000").setDataValidation(statusRule);
  sheet.getRange("O2:O1000").setValue("Pending");

  sheet.setConditionalFormatRules([
    SpreadsheetApp.newConditionalFormatRule().whenTextEqualTo("Pending").setBackground("#FFF9C4").setRanges([sheet.getRange("O2:O1000")]).build(),
    SpreadsheetApp.newConditionalFormatRule().whenTextEqualTo("Verified").setBackground("#C8E6C9").setRanges([sheet.getRange("O2:O1000")]).build(),
    SpreadsheetApp.newConditionalFormatRule().whenTextEqualTo("Receipt Sent").setBackground("#BBDEFB").setRanges([sheet.getRange("O2:O1000")]).build(),
    SpreadsheetApp.newConditionalFormatRule().whenTextEqualTo("Rejected").setBackground("#FFCDD2").setRanges([sheet.getRange("O2:O1000")]).build(),
  ]);

  // --- Settings sheet ---
  var settings = ss.getSheetByName("Settings") || ss.insertSheet("Settings");
  var settingsData = [
    ["Setting",              "Value",                            "Notes"],
    ["Next Receipt Number",  1,                                  "Auto-incremented by the script"],
    ["Receipt Folder ID",    CONFIG.receiptFolderId,             "Google Drive folder for receipt PDFs"],
    ["Config URL",           CONFIG.configUrl,                   "Live site JSON — all org/80G config"],
    ["Receipt Template URL", CONFIG.receiptTemplateUrl,          "Live site HTML receipt template"],
  ];
  settings.getRange(1, 1, settingsData.length, 3).setValues(settingsData);
  settings.getRange(1, 1, 1, 3).setBackground("#4A0E7A").setFontColor("#FFFFFF").setFontWeight("bold");
  settings.setColumnWidth(1, 180);
  settings.setColumnWidth(2, 380);
  settings.setColumnWidth(3, 260);

  SpreadsheetApp.getActiveSpreadsheet().toast("Setup complete! Update CONFIG urls and deploy as Web App.", "✓ Done");
}


