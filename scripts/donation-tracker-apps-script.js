function showSummary() {
  var sheet = SpreadsheetApp.getActiveSpreadsheet().getSheetByName("Donations");
  if (!sheet) {
    SpreadsheetApp.getUi().alert("Donations sheet not found. Run Initial Setup first.");
    return;
  }

  var data      = sheet.getDataRange().getValues();
  var total     = 0, verified = 0, pending = 0;
  var byPurpose = {}, byMonth = {};

  for (var i = 1; i < data.length; i++) {
    var amount  = Number(data[i][7]) || 0;
    var status  = data[i][14];
    var purpose = data[i][11] || "General";
    var date    = data[i][8];

    if (status === "Verified" || status === "Receipt Sent") {
      total += amount;
      verified++;
      byPurpose[purpose] = (byPurpose[purpose] || 0) + amount;
      if (date) {
        var key = Utilities.formatDate(new Date(date), "Asia/Kolkata", "MMM yyyy");
        byMonth[key] = (byMonth[key] || 0) + amount;
      }
    } else if (status === "Pending") {
      pending++;
    }
  }

  var html = "<style>body{font-family:Arial;padding:16px;color:#1a1a1a}"
    + "h2{color:#4A0E7A;margin:0 0 4px}h3{color:#4A0E7A;margin:14px 0 6px;font-size:12px;text-transform:uppercase;letter-spacing:.05em}"
    + ".big{font-size:24px;color:#4A0E7A;font-weight:bold}.stat{display:inline-block;text-align:center;padding:8px 14px}"
    + ".lbl{font-size:10px;color:#888;text-transform:uppercase}table{width:100%;border-collapse:collapse;margin:4px 0}"
    + "td{padding:4px 6px;border-bottom:1px solid #eee;font-size:12px}td:last-child{text-align:right;font-weight:bold;color:#e04d8a}"
    + "</style>";
  html += "<h2>Donation Summary</h2>";
  html += "<div class='stat'><div class='big'>&#8377;" + formatIndianNumber(total) + "</div><div class='lbl'>Total Verified</div></div>";
  html += "<div class='stat'><div class='big'>" + verified + "</div><div class='lbl'>Verified</div></div>";
  html += "<div class='stat'><div class='big'>" + pending + "</div><div class='lbl'>Pending</div></div>";
  html += "<h3>By Purpose</h3><table>";
  for (var p in byPurpose) html += "<tr><td>" + p + "</td><td>&#8377;" + formatIndianNumber(byPurpose[p]) + "</td></tr>";
  html += "</table><h3>By Month</h3><table>";
  for (var m in byMonth) html += "<tr><td>" + m + "</td><td>&#8377;" + formatIndianNumber(byMonth[m]) + "</td></tr>";
  html += "</table>";

  SpreadsheetApp.getUi().showModalDialog(
    HtmlService.createHtmlOutput(html).setWidth(420).setHeight(520),
    "📊 Donation Summary"
  );
}


